# Formatting with leading zeros

# Example 1
num = 10
num = '%03d' % num
print(num)

# RETURN >> 010

# Example 2
nums = []
for x in range(0, 11):
    num = '%03d' % x
    nums.append(num)
print(nums)

# RETURN >> ['000', '001', '002', '003', '004', '005', '006', '007', '008', '009', '010']

